export class I18n {
  constructor(languageMode = 'system') {
    this.languageMode = languageMode;
    this.locale = this.resolveLocale(languageMode);
  }

  setLanguageMode(languageMode) {
    this.languageMode = languageMode;
    this.locale = this.resolveLocale(languageMode);
  }

  resolveLocale(languageMode) {
    if (languageMode === 'es' || languageMode === 'en') return languageMode;
    const browserLanguage = navigator.language?.toLowerCase() ?? 'es';
    return browserLanguage.startsWith('en') ? 'en' : 'es';
  }

  t(key, replacements = {}) {
    const dictionary = translations[this.locale] ?? translations.es;
    const value = dictionary[key] ?? translations.es[key] ?? key;
    return Object.entries(replacements).reduce((text, [name, replacement]) => {
      return text.replaceAll(`{${name}}`, replacement);
    }, value);
  }
}
